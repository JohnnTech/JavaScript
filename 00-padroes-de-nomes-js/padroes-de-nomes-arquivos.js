/*
Exemplo:

utils/
  formatDate.js
  stringUtils.js
PascalCase (comum para componentes React, classes ou arquivos que exportam uma única classe/componente):

Exemplo:


components/
  Header.js
  UserProfile.js

  kebab-case (comum para arquivos HTML, CSS ou em projetos que seguem essa convenção):

Exemplo:


public/
  index.html
  main-style.css

  3. Nomes de Arquivos de Testes
Use o mesmo nome do arquivo principal, seguido de .test ou .spec:

Exemplo:


utils/
  formatDate.js
  formatDate.test.js
Ou use uma pasta separada para testes:


tests/
  formatDate.test.js
4. Nomes de Arquivos de Configuração
Use kebab-case para arquivos de configuração, como:


.eslintrc.js
babel.config.js
webpack.config.js
5. Pastas Especiais
src/: Pasta principal para o código-fonte.

public/: Para arquivos estáticos (HTML, imagens, etc.).

assets/: Para recursos como imagens, ícones, fonts, etc.

components/: Para componentes reutilizáveis (em frameworks como React).

utils/ ou helpers/: Para funções utilitárias.

services/: Para lógica de API ou serviços externos.

tests/ ou __tests__/: Para arquivos de teste.
*/